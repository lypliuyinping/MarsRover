package com.twschool.practice;

    public class Coordinate {
        int x;
        int y;
        public Coordinate(int x, int y){
            this.x=x;
            this.y=y;
        }
        public void setPoint(int x,int y){
            this.x=x;
            this.y=y;
        }
        public int getX()
        {
            return x;
        }
        public int getY(){
            return y;
        }
}
